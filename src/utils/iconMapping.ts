// Helper para obtener el import correcto
export function getHeroIconImport(lucideIcon: string): string {
  const mapping = ICON_MAPPING[lucideIcon];
  if (!mapping) {
    console.warn(`No mapping found for icon: ${lucideIcon}`);
    return '';
  }
  return `@heroicons/react/24/${mapping.path}`;
}
